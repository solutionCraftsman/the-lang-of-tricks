
def celsius_to_fahrenheit(temperature_in_celsius):
    """ function to convert temperature in celsius to fahrenheit """
    # logic here
    # (0°C × 9/5) + 32 = 32°F
    return (temperature_in_celsius * (9 / 5)) + 32


print(celsius_to_fahrenheit(50))


